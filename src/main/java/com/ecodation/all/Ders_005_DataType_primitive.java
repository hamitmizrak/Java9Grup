package com.ecodation.all;

public class Ders_005_DataType_primitive {
	
	public static void main(String[] args) {
		// primitive türler 8 tanedir.
		// f L
		// tam sayılar b < s < i < l
		byte b1 = 127;
		short s1 = 555;
		int i1 = 4444;
		long l1 = 55521L;
		
		// b1=null; bunu yazamam
		
		// virgüllü f < d
		float f2 = 44.23f;
		double d2 = 556.555;
		
		// boolean
		// boolean b3 = true;
		boolean b3 = 3 > 1;
		
		// ASCII Unicode
		char c4 = '\u4523';
		System.out.println(c4);
		
	}
	
}
