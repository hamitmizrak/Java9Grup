package com.ecodation.all;

public class Ders_011_Ternary {
	
	public static void main(String[] args) {
		
		int x, y;
		x = 10;
		y = 9;
		
		if (x == y) {
			System.out.println("Sayılar eşittir");
			
		} else if (x > y) {
			System.out.println("X sayısı Y büyüktür");
			
		} else if (x < y) {
			System.out.println("X sayısı Y küçüktür");
			
		} else {
			System.out.println("Farklı bir sonuc");
			
		}
		
	}
	
}
