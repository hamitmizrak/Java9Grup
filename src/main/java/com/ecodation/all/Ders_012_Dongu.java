package com.ecodation.all;

public class Ders_012_Dongu {
	
	public static void main(String[] args) {
		
		for (int i = 10; i <= 20; i++) {
			System.out.println(i);
			
		}
		System.out.println("*********");
		
		for (int i = 10; i <= 20; i = i + 1) {
			System.out.println(i);
			
		}
		System.out.println("*********");
		for (int i = 10; i <= 20; i += 1) {
			System.out.println(i);
			
		}
		
	}
	
}
