package com.ecodation.all;

public class Ders_013_while {
	
	public static void main(String[] args) {
		
		for (int i = 10; i <= 20; i++) {
			System.out.println(i);
			
		}
		System.out.println("*********");
		
		int y = 5;
		while (y <= 8) {
			System.out.println(y);
			y++;
		}
		
	}
	
}
