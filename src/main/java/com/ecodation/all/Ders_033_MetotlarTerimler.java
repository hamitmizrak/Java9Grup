package com.ecodation.all;

public class Ders_033_MetotlarTerimler {
	// overloading =aşırı yükleme
	public static void deneme() {
		System.out.println("Merhabalar");
		
	}
	
	public static void deneme(int x) {
		System.out.println("Merhabalar");
		
	}
	
	public static void deneme(String x) {
		System.out.println("Merhabalar");
		
	}
	
	public static void deneme(int x, int y) {
		System.out.println("Merhabalar");
		
	}
	
	// overiding
	// @Override =ezmek yani diyor ki benim istediğimi yapacaksın
	
}
